import math

a = min(12,3,4,1,3) # minimum value show
b = max(12,3,4,1,3) # maximum value show
print(a)
print(b)

c = abs(-1110) # negative to positive
print(c)

d = pow(2,3) # Using Power
print(d)

e = math.sqrt(64) # sqrt() method for example, returns the square root of a number
print(e)

x = math.pi
print(x)

x = math.ceil(1.4) # ceil() method rounds a number upwards to its nearest integer
y = math.floor(1.4) #floor() method rounds a number downwards to its nearest integer
print(x)
print(y)