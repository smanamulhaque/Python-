# For Loop
fruits = ("apple", "banana", "cherry","Dog","Egge")
for i in fruits:
    print(i)
print("\n")
# use range
for x in range(len(fruits)):
    print(fruits[x])

print("\n")

# While loop
fruits1 = ("apple", "banana", "cherry","Dog","Egge")
i=0
while i <len(fruits1):
    print(fruits1[i])
    i+=1

